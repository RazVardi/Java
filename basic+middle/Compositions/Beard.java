package middle;

public class Beard { //task 1
	
	private String color;
	private double length;
	
	public Beard(String color, double length) {
		this.color = color;
		this.length = length;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public double getLength() {
		return length;
	}

	public void setLength(double length) {
		this.length = length;
	}
}
